    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if(this.checkInput()){
            String startDate = this.startDateTF.getText();
            String endDate = this.endDateTF.getText();
            String payDate = this.payDateTF.getText();
            int cost = Integer.parseInt(this.costTF.getText().trim());
            int policyTypeId = this.policyTypeCB.getSelectedIndex() == 0?1:2;
            int insuranceTypeId = this.insuranceTypeCB.getSelectedIndex() + 1;
            String mail = (this.customerCB.getSelectedItem().toString().split(" "))[(this.customerCB.getSelectedItem().toString().split(" ")).length - 1];
            int employeeId= this.userId;
            int customerId = this.db.getCustomer(mail).getId();
            this.db.addPolicy(policyTypeId,insuranceTypeId,startDate,payDate, endDate, cost,  employeeId, customerId);
            this.showMessage(AppInfo.SUCCESS_NEW_POLICY, 1);
            MailService.addPolicyMail(mail, this.db.getCustomer(mail).getName());
            this.showMessage(AppInfo.SUCCESS_SEND_MAIL, 1);
            Policy.policies = this.db.getPolicies();
            this.homepage.setTable();
            this.dispose();
        }else{
            this.showMessage(AppInfo.WRONG_INPUT, 0);
        }

    }//GEN-LAST:event_addButtonActionPerformed

