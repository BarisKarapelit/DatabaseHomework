    private void initalSettings() {
        URL iconURL = getClass().getResource(AppInfo.APP_ICON);
        ImageIcon icon = new ImageIcon(iconURL);
        this.setIconImage(icon.getImage());
        this.setTitle(AppInfo.APP_NAME + " - " + AppInfo.ADD_POLICY_PAGE);
        this.appNameLabel.setText(AppInfo.APP_NAME);
        this.versionLabel.setText(AppInfo.VERSION);
        this.addButton.setIcon(new ImageIcon(getClass().getResource(AppInfo.ADD_ICON)));
        this.setCB();
        this.jPanel1.setBackground(AppInfo.APP_COLOR);
        this.setVisible(true);
    }
    
    private void showMessage(String message, int type){
        JOptionPane.showMessageDialog(this, message, AppInfo.APP_NAME, type);
        
    }



    private boolean checkInput() {
       
        if(this.startDateTF.getText().trim().length() == 0){
            return false;
        }
        else if(this.endDateTF.getText().trim().length() == 0){
            return false;
        }
        else if(this.payDateTF.getText().trim().length() == 0){
            return false;
        }
        else if(this.costTF.getText().trim().length() == 0){
            return false;
        }

        return true;
    }

    private void setCB() {
        ArrayList<String> customerNameSurname = this.db.getCustomerNameSurnameEmail();
        this.policyTypeCB.removeAllItems();
        this.insuranceTypeCB.removeAllItems();
        this.customerCB.removeAllItems();
        
        this.policyTypeCB.addItem("indivudual");
        this.policyTypeCB.addItem("entreprise");
        
        this.insuranceTypeCB.addItem("house");
        this.insuranceTypeCB.addItem("car");
        
        for(String info: customerNameSurname){
            this.customerCB.addItem(info);
        }
        
    }
        
       

