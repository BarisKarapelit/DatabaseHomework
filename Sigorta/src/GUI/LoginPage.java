    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        if(this.checkInput()){
            String mail = this.emailTF.getText().trim().toLowerCase();
            String password = this.passwordTF.getText().trim();
            String sys_password = db.getPassword(mail);
            if(sys_password.equals(password)){
                this.showMessage(AppInfo.SUCCESS_LOGIN, 1);
                new HomePage(this.db.getUser(mail));
                this.dispose();
            }else{
                this.showMessage(AppInfo.WRONG_EMAIL_OR_PASSWORD, 0);
            }
        }else{
            this.showMessage(AppInfo.WRONG_INPUT, 0);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

